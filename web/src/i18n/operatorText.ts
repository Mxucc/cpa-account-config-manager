const operatorCatalogs: Record<Locale, Partial<Record<OperatorMessageSource, string>>> = {
  "zh-CN": zhCNOperatorMessages,
  "zh-TW": zhTWOverrides,
  en: {},
  ru: ruOverrides,
};

function isOperatorMessageSource(source: string): source is OperatorMessageSource {
  return Object.prototype.hasOwnProperty.call(zhCNOperatorMessages, source);
}

export function translateOperatorMessage(locale: Locale, source: string): string | undefined {
  if (!isOperatorMessageSource(source)) return undefined;
  return operatorCatalogs[locale][source] ?? source.replaceAll("_", " ");
}
